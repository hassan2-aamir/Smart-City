    public static String formatString(String input) {
        // Split the input string into words
        String[] words = input.split("\\s+");
        
        // Initialize an empty string to build the formatted string
        String formattedText = "<html>";

        // Counter to keep track of words
        int wordCount = 0;

        // Loop through each word in the input string
        for (String word : words) {
            // Append the current word to the formatted text
            formattedText += word + " ";

            // Increment the word count
            wordCount++;

            // Add a line break after every 8 words
            if (wordCount % 8 == 0) {
                formattedText += "<br>";
            }
        }

        // Close the HTML tag
        formattedText += "</html>";

        // Return the formatted string
        return formattedText;
    }
    private void loadImage(String imagePath,javax.swing.JLabel label) {
        try {
            // Load image from file path
            File file = new File(imagePath);
            BufferedImage image = ImageIO.read(file);

            // Scale the image to fit the label
            Image scaledImage = image.getScaledInstance(label.getWidth(), label.getHeight(), Image.SCALE_SMOOTH);

            // Create ImageIcon from the scaled image
            ImageIcon icon = new ImageIcon(scaledImage);

            // Set the ImageIcon as the icon for the label
            label.setIcon(icon);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
    }
    private void loadtext(String text, javax.swing.JLabel label) {
            // Set the text of the label to the provided description
        try{
            text = formatString(text);
            label.setText(text);
        }
        catch (Exception ex) {
            ex.printStackTrace();
        }
    }
